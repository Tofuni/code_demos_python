# params: (arr) list of numbers
def sort_var2(arr):
	c = 0
	while(c != len(arr)-1):
		if (arr[c] > arr[c+1]):
			arr[c], arr[c+1] = arr[c+1], arr[c]
			c = 0
		else:
			c += 1
	return arr

