# params: (data) list of numbers, (percent) percentage as a whole number, (num) number from which to get values within its variance
def within_percent_variance(data, percent, num):
    r = []
    ll = num*(1-percent*0.01)
    ul = num*(1+percent*0.01)
    for a in data:
        if a >= ll and a <= ul:
            r.append(a)
    return r

